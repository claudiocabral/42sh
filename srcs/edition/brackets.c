int		is_open_bracket(char c)
{
	return (c == '\"' || c == '\'' || c == '{' || c == '[' || c == '(');
}

char	get_closing_bracket(char c)
{
	if (c == '\"' || c == '\'')
		return (c);
	else if (c == '[')
		return (']');
	else if (c == '{')
		return ('}');
	else if (c == '(')
		return (')');
	else
		return (0);
}
