#include "libft.h"

int			ft_strchri(const char *s, int c)
{
	int		i;

	i = 0;
	while (s[i] && s[i] != (char)c && s[i] != '\0')
		i++;
	if (s[i] == (char)c)
		return (i);
	else
		return (-1);
}
